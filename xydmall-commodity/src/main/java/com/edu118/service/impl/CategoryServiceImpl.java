package com.edu118.service.impl;

import com.baomidou.mybatisplus.extension.service.impl.ServiceImpl;
import com.edu118.dao.CategoryDao;
import com.edu118.fast.utils.PageUtils;
import com.edu118.fast.utils.Query;
import org.springframework.stereotype.Service;
import java.util.Map;
import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
import com.baomidou.mybatisplus.core.metadata.IPage;

import com.edu118.commodity.entity.CategoryEntity;
import com.edu118.commodity.service.CategoryService;


@Service("categoryService")
public class CategoryServiceImpl extends ServiceImpl<CategoryDao, CategoryEntity> implements CategoryService {

    @Override
    public PageUtils queryPage(Map<String, Object> params) {
        IPage<CategoryEntity> page = this.page(
                new Query<CategoryEntity>().getPage(params),
                new QueryWrapper<CategoryEntity>()
        );

        return new PageUtils(page);
    }

}